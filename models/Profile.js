const mongoose = require('mongoose');

const ProfileSchema = new mongoose.Schema({
  // Reference to 'user' model, cause every profile should be associated with user
  user: {
    type: mongoose.Schema.Types.ObjectId, // Be able to connect profile to id of its user
    ref: 'user',
  },
  company: {
    type: String,
  },
  website: {
    type: String,
  },
  location: {
    type: String,
  },
  // Professional role - Developer, student...
  status: {
    type: String,
    required: true,
  },
  skills: {
    type: [String], // Array of strings
    required: true,
  },
  bio: {
    type: String,
  },
  githubusername: {
    type: String,
  },
  experience: [
    {
      title: {
        type: String,
        required: true,
      },
      company: {
        type: String,
        required: true,
      },
      location: {
        type: String,
      },
      from: {
        type: Date,
        required: true,
      },
      to: {
        type: Date,
      },
      current: {
        type: Boolean,
        default: false,
      },
      description: {
        type: String,
      },
    },
  ],
  education: [
    {
      school: {
        type: String,
        required: true,
      },
      degree: {
        type: String,
        required: true,
      },
      fieldofstudy: {
        type: String,
        required: true,
      },
      from: {
        type: Date,
        required: true,
      },
      to: {
        type: Date,
      },
      current: {
        type: Boolean,
        default: false,
      },
      description: {
        type: String,
      },
    },
  ],
  // Social media links
  social: {
    youtube: {
      type: String,
    },
    twitter: {
      type: String,
    },
    facebook: {
      type: String,
    },
    linkedin: {
      type: String,
    },
    instagram: {
      type: String,
    },
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

// 'profile' is a model name
// so, collection name in MongoDB will be 'profiles' (pluralizes 'profile')
module.exports = Profile = mongoose.model('profile', ProfileSchema);
